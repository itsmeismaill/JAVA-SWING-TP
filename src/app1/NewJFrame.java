    private int heures=0, minutes=0, secondes=0;
    ActionListener taskPerformer=new ActionListener(){
             @Override
             public void actionPerformed(ActionEvent e) {
            // Incrémenter les secondes
            secondes++;

            // Vérifier si les secondes atteignent 60
            if (secondes == 60) {
                secondes = 0;
                minutes++;

                // Vérifier si les minutes atteignent 60
                if (minutes == 60) {
                    minutes = 0;
                    heures++;
                }
            }

            // Mettre à jour le texte de la JLabel avec le temps formaté
            String tempsFormatte = String.format("%02d:%02d:%02d", heures, minutes, secondes);
            jLabel7.setText(tempsFormatte);
        }
    
    };
            private final javax.swing.Timer timer=new Timer(1000,  taskPerformer);

            
            
