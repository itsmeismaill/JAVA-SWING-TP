    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        double cout;
        timer.stop();
        double pu = Double.valueOf(jComboBox2.getSelectedItem().toString());    
        double dur = (double)heures*60+(double)minutes+(double)secondes/60;
        cout = pu*dur;
        if (ferie.isSelected()){
            cout=cout*0.5;
        }
        jTextField2.setText(String.valueOf(cout));
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.addRow(new Object[]{dateformat.format(date),cout});
        double sm=0;
        for(int i =0;i<model.getRowCount();i++){
            String sommecount = jTable1.getValueAt(i,1).toString();
            sm = sm + Double.parseDouble(sommecount);
           jTextField1.setText(Double.toString(sm));
        }
        heures=0;
        minutes=0;
        secondes=0;
        String tempsFormatte = String.format("%02d:%02d:%02d", heures, minutes, secondes);
            jLabel7.setText(tempsFormatte);
        
        
    }//GEN-LAST:event_jButton3ActionPerformed

